/* USER CODE BEGIN PV */
#define PHOTO_THRESHOLD 2000      // ADC threshold
#define BIT_TIMEOUT 150           // ms para decodificar bit largo/corto
#define NUM_BITS 16               // bits de la hora
#define TOKEN_VALIDITY 30

ADC_HandleTypeDef hadc1;
