/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
    MX_USART2_UART_Init();  // inicializar UART2

    uint32_t last_tick = HAL_GetTick();
    uint32_t last_light_state = 0;
    uint8_t received_bits[NUM_BITS];
    uint8_t bit_index = 0;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  // Leer fotoresistencia
	          HAL_ADC_Start(&hadc1);
	          HAL_ADC_PollForConversion(&hadc1, 10);
	          uint32_t adc_val = HAL_ADC_GetValue(&hadc1);

	          uint32_t light_state = (adc_val > PHOTO_THRESHOLD) ? 1 : 0;
	          uint32_t now = HAL_GetTick();

	          // Detectar flanco
	          if(light_state != last_light_state)
	          {
	              uint32_t pulse_width = now - last_tick;
	              last_tick = now;

	              // Decodificar bit según duración
	              if(pulse_width > BIT_TIMEOUT) received_bits[bit_index] = 1;
	              else received_bits[bit_index] = 0;

	              bit_index++;

	              // LED mientras se recibe
	              HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);

	              // Cuando se recibe la hora completa
	              if(bit_index == NUM_BITS)
	              {
	                  // Convertir bits a número
	                  uint32_t new_time = 0;
	                  for(uint8_t i=0;i<NUM_BITS;i++)
	                      new_time = (new_time << 1) | received_bits[i];

	                  clock_stm32 = new_time;  // sincroniza reloj
	                  bit_index = 0;

	                  // Generar y enviar token
	                  uint32_t token = generate_token(clock_stm32);
	                  send_token(token);
	              }
	          }
	          else
	          {
	              HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
	          }

	          last_light_state = light_state;

	          HAL_Delay(10);
	          clock_stm32++; // contador interno
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

